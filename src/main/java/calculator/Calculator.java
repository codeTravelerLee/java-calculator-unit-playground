package calculator;

public class Calculator {
    int add(int n1, int n2) {
        return n1 + n2;
    }

    int subtract(int n1, int n2) {
        return n1 - n2;
    }

    int multiply(int n1, int n2) {
        return n1 * n2;
    }

    int division(int n1, int n2) {
        return n1 / n2;
    }
}
